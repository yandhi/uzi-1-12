package me.kix.uzi.api.util.math;

/**
 * Various math functions used around the client.
 * 
 * @author Jackson
 * @since April 2019
 */
public class MathUtil {

    /**
     * Squares the provided value.
     * 
     * @param value The value to be squared.
     */
    public static double square(double value){
        return value * value;
    }

}